package com.ohgiraffers.section01.polymorphism;

public class 동물 {

    public void 먹기() {
        System.out.println("동물이 먹이를 먹습니다.");
    }

    public void 뛰기() {
        System.out.println("동물이 뛰기 시작합니다.");
    }

    public void 울부짖기() {
        System.out.println("동물이 슬퍼 울부짖기 시작합니다.");
    }

}
